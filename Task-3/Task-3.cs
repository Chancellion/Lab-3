using System;
using System.Collections.Generic;

public class Дорога
{
    public double Довжина { get; set; }
    public double Ширина { get; set; }
    public int КількістьСмуг { get; set; }
    public int ПоточнийРівеньТрафіку { get; set; }
    public Дорога(double довжина, double ширина, int кількістьСмуг, int поточнийРівеньТрафіку)
    {
        Довжина = довжина;
        Ширина = ширина;
        КількістьСмуг = кількістьСмуг;
        ПоточнийРівеньТрафіку = поточнийРівеньТрафіку;
    }
    public override string ToString()
    {
        return $"Дорога: Довжина={Довжина}, Ширина={Ширина}, Кількість смуг={КількістьСмуг}, Поточний рівень трафіку={ПоточнийРівеньТрафіку}";
    }
}
public class ТранспортнийЗасіб
{
    public double Швидкість { get; set; }
    public double Розмір { get; set; }
    public string Тип { get; set; }
    public ТранспортнийЗасіб(double швидкість, double розмір, string тип)
    {
        Швидкість = швидкість;
        Розмір = розмір;
        Тип = тип;
    }
    public override string ToString()
    {
        return $"Транспортний засіб: Швидкість={Швидкість}, Розмір={Розмір}, Тип={Тип}";
    }
}
public interface IDriveable
{
    void Рухатися();
    void Зупинятися();
}
public class Автомобіль : ТранспортнийЗасіб, IDriveable
{
    public Автомобіль(double швидкість, double розмір, string тип) : base(швидкість, розмір, тип) { }
    public void Рухатися()
    {
        Console.WriteLine($"{Тип} рухається зі швидкістю {Швидкість} км/год.");
    }
    public void Зупинятися()
    {
        Console.WriteLine($"{Тип} зупиняється.");
    }
}
public class Симуляція
{
    private List<Дорога> дороги = new List<Дорога>();
    private List<ТранспортнийЗасіб> транспортніЗасоби = new List<ТранспортнийЗасіб>();
    public void ДодатиДорогу(Дорога дорога)
    {
        дороги.Add(дорога);
        Console.WriteLine($"Додано: {дорога}");
    }
    public void ДодатиТранспортнийЗасіб(ТранспортнийЗасіб транспортнийЗасіб)
    {
        транспортніЗасоби.Add(транспортнийЗасіб);
        Console.WriteLine($"Додано: {транспортнийЗасіб}");
    }
    public void ІмітуватиРух()
    {
        foreach (var транспортнийЗасіб in транспортніЗасоби)
        {
            if (транспортнийЗасіб is IDriveable driveable)
            {
                driveable.Рухатися();
            }
        }
    }
    public void ОптимізуватиТрафік()
    {
        Console.WriteLine("Оптимізація трафіку...");
    }
}
public class Програма
{
    public static void Main(string[] args)
    {
        Console.WriteLine("Данiїл Iванченко, КIб-1-23-4.0д");
        Симуляція симуляція = new Симуляція();
        Дорога дорога1 = new Дорога(5.0, 10.0, 3, 50);
        Дорога дорога2 = new Дорога(3.0, 8.0, 2, 30);
        Автомобіль автомобіль1 = new Автомобіль(60.0, 4.5, "Автомобіль");
        Автомобіль автомобіль2 = new Автомобіль(50.0, 4.0, "Автомобіль");
        симуляція.ДодатиДорогу(дорога1);
        симуляція.ДодатиДорогу(дорога2);
        симуляція.ДодатиТранспортнийЗасіб(автомобіль1);
        симуляція.ДодатиТранспортнийЗасіб(автомобіль2);
        симуляція.ІмітуватиРух();
        симуляція.ОптимізуватиТрафік();
    }
}
